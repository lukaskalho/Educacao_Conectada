	<h3>Visão</h3>
	<p>A dimensão de <b>Visão</b> é orientadora do Programa e deve estimular nos estados e municípios o planejamento das ações e tecnologias como elementos transformadores da educação, promovendo valores como: <b>Qualidade</b>, <b>Contemporaneidade</b>, <b>Melhoria de Gestão</b> e qualidade, <b>Dimensão da Visão</b></p>

