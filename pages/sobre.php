	<h2>O que é Educação Conectada?</h2>
	<p>A <b>Educação Conectada</b> é nome do programa de inovação proposta pelo Ministério da Educação para acelerar a incorporação de tecnologias e inovação nas escolas por meio de uma oferta balanceada de conexão a internet, Conteúdos Educacionais Digitais e Formação de Professores.</p>
	
	<h2>Qual é o objetivo do Programa?</h2>
	<p>
		Apoiar a universalização do acesso à internet de alta velocidade e fomentar o uso pedagógico de tecnologias digitais na educação básica, em consonância com a estratégia 7.15 do Plano Nacional de Educação, aprovado pela lei Nº 13.005, de 25 de junho de 2014.
	</p>

	<h2>O que ela traz de novo?</h2>
	<p>
		A principal novidade da <b>Educação Conectada</b> é o reconhecimento de que, além da infraestrutura é necessário disponibilizar conteúdos educacionais digitais, formação de professores e gestores. A política traz:
		<ul>
			<li>Conexão banda larga para pelomenos 22.400 escolas, atendendo a mais de 12 milhões de alunos, na fase inicial do programa.</li>
			
			<li>Oferta inicial de mais de 30.000 conteúdos educacionais digitais em portal integrado, aberto e colaboratívo.</li>
			
			<li>Formação de professores em novas tecnologías.</li>
			
			<li>Formação e suporte técnico para que as escolas façam melhores escolhas na aquisição de tecnologías.</li>
		</ul>
	</p>

	<h2>Como o programa foi instituído?</h2>
	<p>
		O programa foi lançado após processo de construção horizontal e colabortivo, do qual participaram ativamente Secretários de Educação de Estados, Municípios e do Distrito Federal. Sua instituição se deu por meio de decreto Presidencial com envio simultâneo de Projeto de Leia ao Cogresso Nacional.
	</p>

	<h2>Como será implementado o Programa?</h2>
	<p>
		O Ministério da Educação - MEC, implementará o programa conjugando esforços entre orgão e entidades da União, estados, Distrito Federal, municípios, escolas, setor empresarial e sociedade civil, para assegurar as condições necessárias a inserção da tecnologia como ferramenta pedagógica de uso cotidiano nas escolas públcas de educação básica. 
	</p>
	<p>
		O programa está planejado  para ser desenvolvido de 2017 a 2024, de forma a contemplar gradualmente escolas urbanas e rurais, em três grandes fáses: <b>Indução, Expansão e Sustentabilidade</b>
	</p>

